def _safe_parse_json(raw: str) -> dict[str, Any]:
    """Permissive JSON parser. Accepts a bare JSON object, or a code
    fence with JSON inside, or text that contains one ``{...}`` block.
    Returns ``{}`` if nothing usable parses — the caller assigns a
    zero score in that case."""
    if not raw:
        return {}
    s = raw.strip()
    # Strip ```json ... ``` fences if present
    if s.startswith("```"):
        first_nl = s.find("\n")
        if first_nl > 0:
            s = s[first_nl + 1 :]
        if s.endswith("```"):
            s = s[:-3]
        s = s.strip()
    try:
        obj = json.loads(s)
        return obj if isinstance(obj, dict) else {}
    except Exception:
        pass
    # Last resort: lift the first {...} block.
    start = s.find("{")
    end = s.rfind("}")
    if 0 <= start < end:
        try:
            obj = json.loads(s[start : end + 1])
            return obj if isinstance(obj, dict) else {}
        except Exception:
            return {}
    return {}


def _safe_float(value: Any, *, default: float = 0.0) -> float:
    if value is None:
        return default
    try:
        return float(value)
    except (TypeError, ValueError):
        return default
